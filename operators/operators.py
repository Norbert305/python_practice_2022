

# Arithmetic Operators

x = 4

y = 3


add = x + y # returns 7

subtract = x - y # returns 1

multiply = x * y # returns 12

power_of = x ** y # returns 64

divide = x / y # returns 1.333

floor_division = x // y # returns 1 (same thing as / but it returns a int instead of a float)

module = x % 4 # returns remainder of 1





# Assignment Operators

# x is equal to 4

x += 4  # x is 8
x -= 4  # x is 0
x *= 4  # x is 16
x /= 4  # x is 1.0
x %= 4  # x is 0





# Comparison Operators

x = 4
y = 3
 
x == y # returns False
x != y # returns True
x > y  # returns True
x < y  # returns False
x >= y # returns True
x <= y # returns False




# Logical Operators

x = 4
y = 3
 
x > 2 and y > 1      # returns True
x > 5 or y <= 3      # returns True
not(x > 2 and y > 1) # returns False